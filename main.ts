    // Map to store callbacks for button press events
    let buttonCallbacks: { [buttonName: string]: () => void } = {};

    // parent -> [child1, child2, ...]
    let parentChildrenMap: { [parent: string]: string[] } = {};
    
    // parent.child -> value
    let statesMap: { [compositeKey: string]: string } = {};
    
    // parent -> component type (e.g., "B", "S", "TF", "J", "O")
    let componentTypesMap: { [componentName: string]: string } = {};
    
    // update the maps when the app sends data to microbit
    let updateMaps = (name: string, value: string): void => {
        let parentName = "";
        let actualChildName = "";
        let isFound = false;

        // Find the parent of the child (if exists)
        
        // Case A: 1 attribute components
        if (parentChildrenMap[name] && parentChildrenMap[name][0] === "value") {
            parentName = name;
            actualChildName = "value";
            isFound = true;
        } else {
            // Case B: many attriburtes components 
            let keys = Object.keys(parentChildrenMap);
            for (let i = 0; i < keys.length; i++) {
                let p = keys[i];
                if (parentChildrenMap[p].indexOf(name) !== -1) {
                    parentName = p;
                    actualChildName = name;
                    isFound = true;
                    break;
                }
            }
        }

        // Notes on value
        // Button/ToggleButton: [value]. 0 or 1
        // Slider: [value]. number belonging to [min, max]
        // TextField: [value]. value: string
        // Joystick: [angleName, strengthName]. angle: number, strength: number
        // Variable: [value]. value: number (any).

        // Derive key and update values
        let compositeKey = "";
        if (isFound) {
            compositeKey = parentName + "." + actualChildName;
        }

        if (isFound && compositeKey !== "") {
            statesMap[compositeKey] = value;
            if (componentTypesMap[parentName] === "Button") {
                // button always sends 1 when pressed 
                if (buttonCallbacks[parentName]) {
                    buttonCallbacks[parentName]();
                }
                // Auto-reset the button state to "0" after the event
                statesMap[compositeKey] = "0";
            }

        } else {
            console.error("Warning: Received unknown variable " + name);
        }

    }
    
    /**
     * Generates the parent-children map and initializes the states map
     * based on the provided configuration strings.
     * 
     * @param configs The config string generated by the program.
     */
    function generateMaps(configs: string[]): void {
        parentChildrenMap = {};
        statesMap = {};
        componentTypesMap = {};
        for (let i = 0; i < configs.length; i++) {
            let parts = configs[i].split(",");
            if (parts.length == 0) continue;
            
            let type = parts[0];
            
            if (type == "B" || type == "TB") {
                // Button or ToggleButton: B,name or TB,name
                if (parts.length >= 2) {
                    let parentName = parts[1];
                    let childName = "value"; 
                    parentChildrenMap[parentName] = [childName];
                    statesMap[parentName + "." + childName] = "0";
                    componentTypesMap[parentName] = "Button";
                } else {
                    console.error("Setup failed for Button/ToggleButton!");
                }
            } else if (type == "S") {
                // Slider: S,min,max,name
                if (parts.length >= 4) {
                    let parentName = parts[3];
                    let childName = "value";
                    parentChildrenMap[parentName] = [childName];
                    statesMap[parentName + "." + childName] = "0";
                    componentTypesMap[parentName] = "Slider";
                } else {
                    console.error("Setup failed for Slider");
                }
            } else if (type == "TF") {
                // TextField: TF,name
                if (parts.length >= 2) {
                    let parentName = parts[1];
                    // unify all 1-attribute components to have a child called "value" for consistency
                    let childName = "value";
                    parentChildrenMap[parentName] = [childName];
                    statesMap[parentName + "." + childName] = "";
                    componentTypesMap[parentName] = "TextField";
                } else {
                    console.error("Setup failed for TextField");
                }
            } else if (type == "J") {
                // Joystick: J,anglename,smax,strengthName,joystickName
                if (parts.length >= 5) {
                    let angleName = parts[1];
                    // TO CONFIRM: is smax returned??
                    let strengthName = parts[3];
                    let joystickName = parts[4];
                    
                    // joystick has 2 attributes, angleName and strengthName
                    parentChildrenMap[joystickName] = [angleName, strengthName];
                    statesMap[joystickName + "." + angleName] = "0";
                    statesMap[joystickName + "." + strengthName] = "0";
                    componentTypesMap[joystickName] = "Joystick";
                } else {
                    console.error("Setup failed for Joystick");
                }
            } else if (type == "O") {
                // Variables List: O,count,name1,plot1,name2,plot2...
                // each variable is logged as a parent.
                // Note: plotable is NOT changeable once initialized.
                if (parts.length >= 2) {
                    let count = parseInt(parts[1]);
                    for (let j = 0; j < count; j++) {
                        let nameIdx = 2 + (j * 2);
                        if (nameIdx < parts.length) {
                            let varName = parts[nameIdx];
                            parentChildrenMap[varName] = ["value"];
                            statesMap[varName + ".value"] = "0";
                            componentTypesMap[varName] = "Variable";
                        }
                    }
                } else {
                    console.error("Setup failed for Variables");
                }
            } else {
                console.error("Unknown config type: " + type);
            }
        }
    }

    /**
     * Checks if a variable name was actually defined in the configuration.
     */
    function isValidVariable(name: string): boolean {
        // Case A: only 1 data attribute
        if (parentChildrenMap[name] && parentChildrenMap[name][0] === "value") {
            return true;
        }
        
        // Case B: many data attributes
        let keys = Object.keys(parentChildrenMap);
        for (let i = 0; i < keys.length; i++) {
            if (parentChildrenMap[keys[i]].indexOf(name) !== -1) {
                return true;
            }
        }
        
        return false;
    }
    
    /**
     * Ensures a name is at most 15 characters long.
     * If it is longer, it truncates it to the first 15 characters.
     * @param name The name to sanitize
     */
    function sanitizeName(name: string): string {
        // the technical reason for this is because the bluetooth has a 60-byte buffer 
        // and we cannot allow names to be too long else we will have not enough space to receive data.
        // interestingly, i think it is only a receive buffer issue? but it is too much work (and quite difficult) to 
        // keep scraping for any data on uart receive. so we will use this as a workaround.
        if (name.length > 15) {
            return helpers.stringSlice(name, 0, 15);
        }
        return name;
    }
    
