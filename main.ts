    /**
     * Creates a slider
     * @param min The minimum value of slider (>0), eg: 1
     * @param max The maximum value of slider (<=255), eg: 255
     * @param name The name of the slider
     */
    //% color="#f150f1"
    //% min.defl=0
    //% max.defl=255
    //% name.defl="Slider"
    //% blockId=pkscontroller_bluetooth_slider block="create slider $min $max $name" subcategory="Bluetooth"
    //% group="Configuration"
    export function createSlider(min: number, max: number, name: string): string {

        // silently change min/max if they are set to the wrong value
        if (min > max) {
            const temp = min
            min = max
            max = temp
        }
        const output: string = `S,${min},${max},${sanitizeName(name)}`
        return output

    }

